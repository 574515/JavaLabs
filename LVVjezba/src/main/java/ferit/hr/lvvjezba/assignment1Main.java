    public void initialSetup() {
        this.setLocationRelativeTo(null);        
        ButtonGroup grp = new ButtonGroup();
        grp.add(rbRandom);
        grp.add(rbDefine);
        isLimited = false;
        txtGuess.setEnabled(false);
        txtGuess.setText("");        
        bttnGuess.setEnabled(false);
        rbRandom.setSelected(true);        
        lblRange.setVisible(false);
        lblRangeValues.setText("");        
        lblNumOfTries.setVisible(false);
        lblNumOfTriesValue.setText("");        
        lblStatus.setVisible(false);
        lblStatusTxt.setText("");        
        rbRandom.setEnabled(true);
        rbDefine.setEnabled(true);
        bttnStart.setEnabled(true);
        cbLimitTries.setEnabled(true);
        cbLimitTries.setSelected(false);
    }
        
    public static int defineNumber(int min, int max) {
        return new Random().nextInt(max - min) + min;
    }
    
    public void goWin() {
        String message;
        if (!isLimited) {
            message = "Congratulations!\nNumber of tries: " + tries + ".\nDo you want to play again?";
        }
        else {
            message = "Congratulations!\nNumber of tries: [" + tries + " / " + limitedTries + "].\nDo you want to play again?";
        }
        if (JOptionPane.showConfirmDialog(this, message, "Winner!", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            initialSetup();
        }
        else {
            this.dispose();
        }
    }
    
    public void goLose() {
        String message;
        if (!isLimited) {
            message = "Sadly, you lost.\nNumber of tries: " + tries + ".\nDo you want to play again?";
        }
        else {
            message = "Sadly, you lost.\nNumber of tries: [" + tries + " / " + limitedTries + "].\nDo you want to play again?";
        }
        if (JOptionPane.showConfirmDialog(this, message, "Loser!", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            initialSetup();
        }
        else {
            this.dispose();
        }
    }

    public void guess() {
        int guess = Integer.parseInt(txtGuess.getText());
        if (guess > guessNumber) {
            tries++;
            lblStatusTxt.setText("Lower...");
            if (isLimited) {
                lblNumOfTriesValue.setText("[" + tries + " / " + limitedTries + "]");
            }
            else {
                lblNumOfTriesValue.setText("" + tries);
            }            
            if (isLimited && (tries == limitedTries)) {
                goLose();
            }
        } else if (guess < guessNumber) {
            tries++;
            lblStatusTxt.setText("Higher...");
            if (isLimited) {
                lblNumOfTriesValue.setText("[" + tries + " / " + limitedTries + "]");
            }
            else {
                lblNumOfTriesValue.setText("" + tries);
            }            
            if (isLimited && (tries == limitedTries)) {
                goLose();
            }
        } else {
            tries++;
            if (isLimited) {
                lblNumOfTriesValue.setText("[" + tries + " / " + limitedTries + "]");
            }
            else {
                lblNumOfTriesValue.setText("" + tries);
            }
            goWin();
        }
    }
    
