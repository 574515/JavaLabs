    private void bttnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnStartActionPerformed
        if (rbRandom.isSelected()) {
            guessNumber = defineNumber(0, (definedMax + 1));
            lblRangeValues.setText("[0, " + definedMax + "]");
        } else if (rbDefine.isSelected()) {
            int min, max;
            do {
                min = Integer.parseInt(JOptionPane.showInputDialog(this, "Enter minimum (inclusive)"));
            } while(min < 0);
            do {
                max = Integer.parseInt(JOptionPane.showInputDialog(this, "Enter maximum (exclusive)"));
            } while ((max <= min) || (max > Integer.MAX_VALUE));
            guessNumber = defineNumber(min, max);
            lblRangeValues.setText("[" + min + ", " + (max - 1) + "]");
        }
        tries = 0;
        cbLimitTries.setEnabled(true);
        rbRandom.setEnabled(false);
        rbDefine.setEnabled(false);
        bttnStart.setEnabled(false);
        cbLimitTries.setEnabled(false);
        txtGuess.setEnabled(true);
        txtGuess.requestFocus();
        bttnGuess.setEnabled(true);
        lblRange.setVisible(true);
        lblNumOfTries.setVisible(true);
        lblStatus.setVisible(true);
        if (cbLimitTries.isSelected()) {
            isLimited = true;
            lblNumOfTriesValue.setText("[" + tries + " / " + limitedTries + "]");
        }
        else lblNumOfTriesValue.setText("" + tries);
        lblStatusTxt.setText("Guess");

        bttnStart.setText("" + guessNumber);
    }//GEN-LAST:event_bttnStartActionPerformed

